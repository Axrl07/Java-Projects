    private void iniciarButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarButtomActionPerformed
        String IngresoCodigoField = usuarioField.getText();
        String IngresoClaveField = claveField.getText();
        if (IngresoCodigoField.equals("admin") && IngresoClaveField.equals("admin")) {
            adminVentana accederVentanaA = new adminVentana();
            this.dispose();
            return;
        } else {
            int IngresoCodigo = Integer.parseInt(IngresoCodigoField);
            for (Profesor profe : listadoProfesores) {
                if(IngresoCodigo == profe.codigo){
                    ProfesoresVentana accederVentanaE = new ProfesoresVentana(IngresoCodigo);
                    this.dispose();
                    return;
                }
            }
            for (Estudiante estu : listadoEstudiantes) {
                if(IngresoCodigo == estu.codigo){
                    EstudiantesVentana accederVentanaE = new EstudiantesVentana(IngresoCodigo);
                    this.dispose();
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(null, "Usuario o clave incorrectos", "Datos incorrectos", JOptionPane.INFORMATION_MESSAGE);        
    }//GEN-LAST:event_iniciarButtomActionPerformed

