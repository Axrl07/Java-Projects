    public Estudiante enlace_CodigoEstu(int codigo){
        for (Estudiante e : listadoEstudiantes) {
            if (e.codigo == codigo) {
                return e;
            }
        }
        return null;
    }
    
    public void BotonesDinamicos(){
        for (Asignatura curso : listadoAsignaturas) {
            for (Notas integrante : curso.integrantes) {
                if(integrante.estudiante == this.codigoUsuario){
                    crearBotones(curso);
                }
            }
        }
    }
     
            
    int index = 0;
    
    public void crearBotones(Asignatura curso){
        JButton botonCurso = new JButton(curso.nombre);
        JLabel numEs = new JLabel(curso.integrantes.size()+" estudiantes");
        botonCurso.setBounds(20, 20 + 80*index + 20*index, 250, 80);
        // Agregar una accion al presionar el boton
        botonCurso.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                System.out.println("hola");
                AdminCursoVentana acceder = new AdminCursoVentana(curso, codigoUsuario);
            }
        });
        numEs.setBounds(120, 90 + 80*index + 20*index, 100, 30);
        this.jPanel2.add(botonCurso);
        this.jPanel2.add(numEs);
        this.index += 1;
    }
    
    
    
    
