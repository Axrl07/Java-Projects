    private void llenarTablaDatos(Profesor ingresoP, Estudiante ingresoE){
        if (ingresoE != null) {
            DefaultTableModel modeloTabla = new DefaultTableModel();
            modeloTabla.addColumn("Código");
            modeloTabla.addColumn("Nombre");
            modeloTabla.addColumn("Apellido");
            modeloTabla.addColumn("Correo");
            modeloTabla.addColumn("Clave");        
            modeloTabla.addColumn("Género");
            Object[] filaDatos = { ingresoE.codigo, ingresoE.nombre, ingresoE.apellido, ingresoE.correo, ingresoE.clave, ingresoE.genero};
            modeloTabla.addRow(filaDatos);
            tablaUsuario.setModel(modeloTabla);
        } else {
            DefaultTableModel modeloTabla = new DefaultTableModel();
            modeloTabla.addColumn("Código");
            modeloTabla.addColumn("Nombre");
            modeloTabla.addColumn("Apellido");
            modeloTabla.addColumn("Correo");
            modeloTabla.addColumn("Clave");        
            modeloTabla.addColumn("Género");
            Object[] filaDatos = { ingresoP.codigo, ingresoP.nombre, ingresoP.apellido, ingresoP.correo, ingresoP.clave,ingresoP.genero};
            modeloTabla.addRow(filaDatos);
            tablaUsuario.setModel(modeloTabla);
        }
    }
