    private void ActualizarBottomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarBottomActionPerformed
        String nombre = nombreF.getText();
        String apellido = ApellidoF.getText();
        String correo = CorreoF.getText();
        String clave = ClaveF.getText();
        String auxGenero = (String) DesplegableComboBox.getSelectedItem();
        String auxGenero2 = auxGenero.toLowerCase();
        char genero = auxGenero2.charAt(0);
        if(this.alumno != null){
            for (int i = 0; i < listadoProfesores.size(); i++) {
                Profesor objeto = listadoProfesores.get(i);
                if(this.profe.codigo == objeto.codigo){
                    objeto.nombre = nombre;
                    objeto.apellido = apellido;
                    objeto.correo = correo;
                    objeto.clave = clave;
                    objeto.genero = genero;
                    listadoProfesores.set(i, objeto);
                    JOptionPane.showMessageDialog(null, "Se han actualizado los datos del profesor", "Actualizar Profesor", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                }
            }
        }else{
            for (int i = 0; i < listadoProfesores.size(); i++) {
                Profesor objeto = listadoProfesores.get(i);
                if(this.profe.codigo == objeto.codigo){
                    objeto.nombre = nombre;
                    objeto.apellido = apellido;
                    objeto.correo = correo;
                    objeto.clave = clave;
                    objeto.genero = genero;
                    listadoProfesores.set(i, objeto);
                    JOptionPane.showMessageDialog(null, "Se han actualizado los datos del profesor", "Actualizar Profesor", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_ActualizarBottomActionPerformed

